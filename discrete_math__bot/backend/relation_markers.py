# Маркеры для автоматического определения типов отношений
RELATION_MARKERS = {
    "AGGREGATION": [
        "часть", "состоит из", "содержит", "включает", "компонент", "элемент",
        "разделяется на", "подразделяется на", "делится на"
    ],
    "COMPOSITION": [
        "образует", "порождает", "строится из", "создается из", "входит в состав",
        "неотъемлемая часть", "является объединением"
    ],
    "ASSOCIATION": [
        "связан", "связано", "используется", "применяется", "относится", "основан на",
        "соответствует", "зависит от"
    ],
    "REALIZES": [
        "реализует", "вычисляет", "находит", "служит для", "позволяет"
    ]
}

def detect_relation_from_text(text):
    """Возвращает тип отношения по наличию маркеров в тексте."""
    text_lower = text.lower()
    for rel_type, markers in RELATION_MARKERS.items():
        for marker in markers:
            if marker in text_lower:
                return rel_type
    return None